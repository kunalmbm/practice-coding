package ds.list.single;

public class NodeTuple {
	
	private Node left;
	private Node center;
	private Node right;
	
	public NodeTuple(Node left,Node center,Node right) {
		this.left = left;
		this.center = center;
		this.right = right;
	}

	public Node getLeft() {
		return left;
	}

	public Node getCenter() {
		return center;
	}

	public Node getRight() {
		return right;
	}
	
	
	
	

}
